	<h2>Sthala Puranam</h2>
	<p>After Hiranyasamhara by the Lord, all the devas praised the strength and courage (Balam &amp; Showyam) of the Lord, as <i>“Ahoveeryam, Ahoshowryam Ahobahupa rakramam Narasimham, Paramdaivam <b>Ahobilam Ahobalam</b>”</i></p>
	<p>Lord Rama, while searching for Seetha, was stated to have worshipped Lord Narasimha here with His Panchamrita Stotram. Lord Balaji offered Nivedyam to Lord Narasimha, during His wedding to Padmavathi Thayar at Tirumala Hills. Lord Shiva praised Lord Narasimha as &#8216;Supreme God&#8217; in His Mantrarajapada Stotram. Rudra has a small shrine adjacent to Sri Ahobila Narasimha sannidhi. Garuda did Tapas here and the Lord appeared before him. Navagrahas have worshipped the Nava Narasimha Moorthies at Ahobilam. They were relieved of their problems from Asuras, and from the curses they received from Maharishis. It is believed that Chandra (Moon) took to the worship of Sri Karanja Narasimha, Mars to Jwala Narasimha and so on. Adi Sankara was protected from danger to his life and he glorified the Lord Narasimha by his famous Karavalamba Stotram. Thirumangai Alwar had sung in praise of Lord Narasimha with ten Pasurams. Acharya Ramanuja and Swami Desika worshipped the Lord here.</p>
	<p>Pilgrims who visit Ahobilam are blessed with immense benefits. Stage Narada averred that whoever stays one night in Ahobilam derives the same benefit that accrues to an individual staying in Kasi for a thousand yugas, in Prayag for twenty yugas and in Gaya for a hundred yugas!</p>

